/// Spacing constants following the design system scale
/// Base unit: 4px for consistent visual harmony
class AppSpacing {
  AppSpacing._();

  /// 2px - Extra extra small spacing
  static const double spaceXXS = 2.0;

  /// 4px - Extra small spacing
  static const double spaceXS = 4.0;

  /// 8px - Small spacing
  static const double spaceS = 8.0;

  /// 12px - Medium spacing
  static const double spaceM = 12.0;

  /// 16px - Large spacing
  static const double spaceL = 16.0;

  /// 24px - Extra large spacing
  static const double spaceXL = 24.0;

  /// 32px - Extra extra large spacing
  static const double spaceXXL = 32.0;

  /// 48px - Extra extra extra large spacing
  static const double spaceXXXL = 48.0;
}